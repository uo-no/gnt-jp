/* 日付ベースで決定的に1件選択（UTC日付 mod 365）*/
function getDailyVerse() {
    const today    = new Date();
    const dayIndex = Math.floor(
        Date.UTC(today.getFullYear(), today.getMonth(), today.getDate()) / 86400000
    );
    return DAILY_VERSES_365[dayIndex % 365];
}
